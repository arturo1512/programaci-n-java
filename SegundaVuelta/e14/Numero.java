public class Numero{
	public int a;
	public int b;
	public int c;
    public int suma;
    public static int div=3;
	public Numero(int a, int b, int c){
		this.a = a;
		this.b = b;
		this.c = c;
	}

	public void setA(int a){
		this.a = a;
	}
	public int getA(){
		return this.a;
	}

	public void setB(int b){
		this.b = b;
	}
	public int getB(){
		return this.b;
	}
	public void setC(int c){
		this.c =c;
	}
	public int getC(){
		return this.c;
	}

	public int getSuma(){
	return	this.div /= this.suma = this.a + this.b + this.c;

	}
}