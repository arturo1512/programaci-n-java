public class CA{

    protected int x,y;
    public static int suma;

    public CA(int x_p, int y_p){
    	this.x = x_p;
    	this.y = y_p;
    }
    public void setX(int x_p){
    	this.x = x_p;
    }
    public int getX(){
    	return this.x;
    }
    public void setY(int y_p){
    	this.y = y_p;
    }
    public int getY(){
    	return this.y;
    }
    public void setSuma(int suma_p){
    	this.suma = suma_p;
    }
    public  int getSuma(){
    	return this.suma = this.x += this.y;
    }




}